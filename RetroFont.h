// Indeks znaku w tablicy; -1 = nie ma takiego glifu (rysowanie go pomija).
inline int index(char c) {
  if (c >= 'a' && c <= 'z') c = static_cast<char>(c - 'a' + 'A');
  for (int i = 0; i < COUNT; ++i) if (FIRST_CHARS[i] == c) return i;
  return -1;
}
